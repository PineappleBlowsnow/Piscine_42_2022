void	ft_putchar(char c);

void	ft_print_map(int **tab)
{
	int	x;
	int	y;

	x = 0;
	y = 0;
	while (x < 4)
	{
		y = 0;
		while (y < 4)
		{
			ft_putchar('0' + tab[x][y]);
			if (y != 3)
				ft_putchar(' ');
			else
				ft_putchar('\n');
			y++;
		}
		x++;
	}
}
