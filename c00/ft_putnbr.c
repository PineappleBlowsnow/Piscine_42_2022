#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;

	if (nb == 0)
		write(1, "0", 1);
	if (nb < 0)
	{
		write(1, "-", 1);
		c = '0' - (nb % 10);
		nb = nb / 10;
		nb = -nb;
		if (nb != 0)
			ft_putnbr(nb);
		write(1, &c, 1);
	}
	else
	{
		c = '0' + (nb % 10);
		nb = nb / 10;
		if (nb != 0)
			ft_putnbr(nb);
		write(1, &c, 1);
	}
}
